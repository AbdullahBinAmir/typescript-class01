const myCountry: string = 'Pakistan'

const num1: number = 1

// Tests for equality and inequality with strings

console.log(myCountry!=='Pakistan')

console.log(myCountry==='Pakistan')

// Tests using the lower case function
console.log(myCountry.toLowerCase())

//Numerical tests involving equality and inequality, greater than and less than, greater than or equal to, and less than or equal to

console.log(num1===1)

console.log(num1!==1)

console.log(num1<=1)

console.log(num1>=1)

//Tests using "and" and "or" operators

console.log(num1>1 && true)

console.log(num1>1 || true)

// whether an item is an array

console.log(Array.isArray(myCountry))

// whether an item is not an array

console.log(!Array.isArray(myCountry))


